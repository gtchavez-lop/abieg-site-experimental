<script>
	import { fly, fade, scale, blur } from 'svelte/transition';
	import { goto } from '$app/navigation';

	import { onMount } from 'svelte';
	import { supabase } from '../../../global';
	import AdminPostCard from '../../../components/AdminPostCard.svelte';
	import toastify from 'toastify-js';
	import 'toastify-js/src/toastify.css';

	// component variables
	let hasBlog = null;
	let blogs;
	let tabActive = 2;
	let hasAccount;
	let user = supabase.auth.user();

	let blog_title;
	let blog_imageURI;
	let blog_content;
	let blog_visibility = false;

	// methods
	onMount(async (e) => {
		if (supabase.auth.user()) {
			if (supabase.auth.user().role == 'authenticated') {
				let user = supabase.auth.user();
				let { data, error } = await supabase.from('users').select('*').eq('id', user.id);
				if (data[0].isModerator == true || data[0].isAdmin == true) {
					hasAccount = true;
				} else {
					goto('/admin');
				}
			}

			if (hasAccount) {
				getPosts();
			}
		} else {
			goto('/admin');
		}
	});

	const getPosts = async (e) => {
		const { data, error } = await supabase
			.from('posts')
			.select('*')
			.eq('author', user.email.split('@')[0]);

		hasBlog = null;
		if (error || data.length < 1) {
			hasBlog = false;
		}
		if (!error || data.length > 0) {
			blogs = data;
			hasBlog = true;
		}
	};

	let postBlog = async (e) => {
		if (blog_title && blog_content) {
			const { data, error } = await supabase.from('posts').insert([
				{
					title: blog_title,
					author: user.email.split('@')[0],
					content: blog_content,
					header_img: blog_imageURI ? blog_imageURI : 'https://picsum.photos/500/500',
					isExclusive: blog_visibility
				}
			]);

			if (!error) {
				toastify({
					text: `${blog_title} is now posted ${!blog_visibility ? 'publicly' : 'exclusively'}`,
					duration: 2000,
					close: true,
					gravity: 'bottom',
					position: 'right',
					style: {
						background: '#06d6a0',
						color: '#212529'
					}
				}).showToast();
				blog_title = '';
				blog_content = '';
				blog_visibility = false;
				tabActive = 2;
				getPosts();
			}
		} else {
			// toast.message('Please fill out all forms');
			toastify({
				text: 'Please fill the Title and the Description',
				duration: 2000,
				close: true,
				gravity: 'bottom',
				position: 'right',
				style: {
					background: '#ef476f'
				}
			}).showToast();
		}
	};
</script>

<svele:head>
	<title>Dashboard | Abie G</title>
</svele:head>
<main
	style="margin-bottom: 10em;"
	in:fly={{ y: -40, duration: 500, delay: 500 }}
	out:fly={{ y: 40, duration: 500 }}
>
	<div class="container text-white">
		<p class="display-3">Moderator Dashboard</p>

		<div class="btn-group mt-3 w-100">
			<button
				on:click={(e) => {
					tabActive = 1;
				}}
				type="button"
				class="btn btn-lg btn-outline-primary">Add a Story</button
			>
			<button
				on:click={(e) => {
					tabActive = 2;
				}}
				type="button"
				class="btn btn-lg btn-outline-primary">Your Stories</button
			>
		</div>

		<!-- tabs -->
		<div class="mt-5">
			<!-- add story -->
			{#if tabActive == 1}
				<div in:fly={{ x: 20, duration: 500 }}>
					<p class="display-5">Add a story</p>
					<div class="row mt-3">
						<div class="col-12">
							<div class="form-floating mb-3">
								<input
									type="text"
									class="form-control bg-transparent text-white"
									id="story_title"
									bind:value={blog_title}
								/>
								<label for="story_title">The Title of your story</label>
							</div>
						</div>
						{#if blog_imageURI}
							<div class="col-12 mb-1">
								<img
									style="width: 100%; height: 250px; object-fit: cover;"
									src={blog_imageURI != ''
										? blog_imageURI
										: 'https://via.placeholder.com/1500?text=This+is+a+placeholder+image'}
									alt="..."
								/>
							</div>
						{/if}
						<div class="col-12">
							<div class="form-floating mb-3">
								<input
									type="text"
									class="form-control bg-transparent text-white"
									id="story_imageUri"
									bind:value={blog_imageURI}
								/>
								<label for="story_imageUri">Story Header Image URL</label>
							</div>
							<p class="italic">
								*If you leave this blank, the site will generate a placeholder image
							</p>
						</div>
						<div class="col-12">
							<div class="form-floating mb-3">
								<textarea
									type="text"
									style="min-height: 10em;"
									class="form-control bg-transparent text-white"
									id="story_content"
									bind:value={blog_content}
								/>
								<label for="story_content">Story content</label>
							</div>
							<p class="italic">*We prefer you to write the story content in HTML</p>
						</div>
						<div class="col-12 mt-3">
							<div class="form-check form-switch">
								<input
									class="form-check-input"
									bind:checked={blog_visibility}
									type="checkbox"
									id="toggleVisibility"
								/>
								<label class="form-check-label" for="toggleVisibility">
									{#if blog_visibility}
										Exclusive Content
									{:else}
										Public Content
									{/if}
								</label>
							</div>
						</div>
						<div class="col-12 mt-5">
							<button on:click={postBlog} class="btn btn-primary">Post your story</button>
						</div>
					</div>
				</div>
			{/if}
			<!-- view stories -->
			{#if tabActive == 2}
				<div in:fly={{ x: 20, duration: 500 }}>
					<p class="display-5">Your Stories</p>
					<div class="row text-white">
						{#if hasBlog == null}
							<div class="spinner-border text-info" role="status">
								<span class="visually-hidden">Loading...</span>
							</div>
						{/if}
						{#if !hasBlog || blogs.length < 1}
							<div class="col-12">
								<h5>Seems like its empty</h5>
								<p>Make one of your own</p>
							</div>
						{:else}
							<div class="col-12">
								<div class="mt-1 row row-cols-1 row-cols-md-2 g-3">
									{#each blogs as blog, index}
										<AdminPostCard {blog} {index} />
									{/each}
								</div>
							</div>
						{/if}
					</div>
				</div>
			{/if}
		</div>
	</div>
</main>

